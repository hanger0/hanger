package com.hanger.manager.managerPage.vo;

public class ManagerPeriodSalesVo {
	private String period;
	private String totalSellPrice;
	private String totalPurchasePrice;
	private String totalUsedMileage;
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getTotalSellPrice() {
		return totalSellPrice;
	}
	public void setTotalSellPrice(String totalSellPrice) {
		this.totalSellPrice = totalSellPrice;
	}
	public String getTotalPurchasePrice() {
		return totalPurchasePrice;
	}
	public void setTotalPurchasePrice(String totalPurchasePrice) {
		this.totalPurchasePrice = totalPurchasePrice;
	}
	public String getTotalUsedMileage() {
		return totalUsedMileage;
	}
	public void setTotalUsedMileage(String totalUsedMileage) {
		this.totalUsedMileage = totalUsedMileage;
	}
	
	
}
